    let displayGapR = node.gaps.right;
    let displayGapT = node.gaps.top;
    let displayGapB = node.gaps.bottom;
    let colNodes = getColNodes(node.id);
    colNodes.forEach(n => {
        let e = edges.find(ed => ed.to === n.id);
        if (e && (e.dir === 'left' || e.dir === 'right')) {
            let parentX = nodes.find(pn => pn.id === e.from);
            if (e.dir === 'right') displayGapL = parentX.gaps.right;
            if (e.dir === 'left') displayGapR = parentX.gaps.left;
        }
    });
    let rowNodes = getRowNodes(node.id);
    rowNodes.forEach(n => {
        let e = edges.find(ed => ed.to === n.id);
        if (e && (e.dir === 'top' || e.dir === 'bottom')) {
            let parentY = nodes.find(pn => pn.id === e.from);
            if (e.dir === 'bottom') displayGapT = parentY.gaps.bottom;
            if (e.dir === 'top') displayGapB = parentY.gaps.top;
        }
    });
    document.getElementById('set-gap-l').value = displayGapL;
    document.getElementById('set-gap-r').value = displayGapR;
    document.getElementById('set-gap-t').value = displayGapT;
    document.getElementById('set-gap-b').value = displayGapB;
    document.getElementById('set-note-height').value = node.settings.noteHeight;
