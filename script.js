function liveUpdate() {
    if (!currentNodeId) return;
    const node = nodes.find(n => n.id === currentNodeId);
    let newBg = document.getElementById('set-bg').value;
    let newStroke = document.getElementById('set-stroke').value;
    let newColor = document.getElementById('set-color').value;
    if (node.settings.bg.toLowerCase() !== newBg.toLowerCase() ||
        node.settings.stroke.toLowerCase() !== newStroke.toLowerCase() ||
        node.settings.color.toLowerCase() !== newColor.toLowerCase()) {
        node.settings.isCustomColor = true;
    }
    node.settings.bg = newBg;
    node.settings.stroke = newStroke;
    node.settings.color = newColor;
    node.width = parseFloat(document.getElementById('set-width').value) || defaultWidthFr;
    node.height = parseFloat(document.getElementById('set-height').value) || defaultHeightFr;
    let valL = parseFloat(document.getElementById('set-gap-l').value);
    let valR = parseFloat(document.getElementById('set-gap-r').value);
    let valT = parseFloat(document.getElementById('set-gap-t').value);
    let valB = parseFloat(document.getElementById('set-gap-b').value);
    let newGapL = isNaN(valL) ? defaultGapFr : valL;
    let newGapR = isNaN(valR) ? defaultGapFr : valR;
    let newGapT = isNaN(valT) ? defaultGapFr : valT;
    let newGapB = isNaN(valB) ? defaultGapFr : valB;
    let colNodes = getColNodes(node.id);
    colNodes.forEach(n => {
        n.gaps.left = newGapL;
        n.gaps.right = newGapR;
        let inEdge = edges.find(e => e.to === n.id);
        if (inEdge) {
            let parentNode = nodes.find(pn => pn.id === inEdge.from);
            if (inEdge.dir === 'right') parentNode.gaps.right = newGapL;
            if (inEdge.dir === 'left') parentNode.gaps.left = newGapR;
        }
    });
    let rowNodes = getRowNodes(node.id);
    rowNodes.forEach(n => {
        n.gaps.top = newGapT;
        n.gaps.bottom = newGapB;
        let inEdge = edges.find(e => e.to === n.id);
        if (inEdge) {
            let parentNode = nodes.find(pn => pn.id === inEdge.from);
            if (inEdge.dir === 'bottom') parentNode.gaps.bottom = newGapT;
            if (inEdge.dir === 'top') parentNode.gaps.top = newGapB;
        }
    });
    node.settings.noteHeight = parseFloat(document.getElementById('set-note-height').value) || defaultNoteHFr;
    recalculatePositions();
    render();
    animateEdges();
}
function drawEdges(ctx, forExport = false) {
    if (!forExport) ctx.clearRect(0, 0, ctx.canvas.width, ctx.canvas.height);
    let groups = {};
    edges.forEach(edge => {
        let key = edge.from + '_' + edge.dir;
        if (!groups[key]) groups[key] = [];
        groups[key].push(edge);
    });
    for (let key in groups) {
        let group = groups[key];
        let fromNode = nodes.find(n => n.id === group[0].from);
        if (!fromNode) continue;
        let fx = fromNode.x;
        let fy = fromNode.y;
        let fromW = fromNode.width * frSize;
        let fromH = fromNode.height * frSize;
        let noteH = fromNode.note.show ? (fromNode.settings.noteHeight * frSize) - 2 : 0;
        let dir = group[0].dir;
        let startX, startY;
        if (dir === 'right') { startX = fx + fromW; startY = fy + fromH / 2; }
        else if (dir === 'left') { startX = fx; startY = fy + fromH / 2; }
        else if (dir === 'bottom') { startX = fx + fromW / 2; startY = fy + fromH + noteH; }
        else if (dir === 'top') { startX = fx + fromW / 2; startY = fy; }
        let drawStartX = startX + (forExport ? 0 : cameraX);
        let drawStartY = startY + (forExport ? 0 : cameraY);
        let endPoints = [];
        group.forEach(edge => {
            let toNode = nodes.find(n => n.id === edge.to);
            if (!toNode) return;
            let tx = toNode.x;
            let ty = toNode.y;
            let toW = toNode.width * frSize;
            let toH = toNode.height * frSize;
            let toNoteH = toNode.note.show ? (toNode.settings.noteHeight * frSize) - 2 : 0;
            let endX, endY;
            if (dir === 'right') { endX = tx; endY = ty + toH / 2; }
            else if (dir === 'left') { endX = tx + toW; endY = ty + toH / 2; }
            else if (dir === 'bottom') { endX = tx + toW / 2; endY = ty; }
            else if (dir === 'top') { endX = tx + toW / 2; endY = ty + toH + toNoteH; }
            endPoints.push({ x: endX + (forExport ? 0 : cameraX), y: endY + (forExport ? 0 : cameraY) });
        });
        if (endPoints.length === 0) continue;
        ctx.beginPath();
        ctx.strokeStyle = fromNode.settings.stroke;
        ctx.lineWidth = 2;
        if (dir === 'right' || dir === 'left') {
            let gap = (dir === 'right') ? fromNode.gaps.right * frSize : fromNode.gaps.left * frSize;
            let offset = gap / 2;
            let midX = (dir === 'right') ? drawStartX + offset : drawStartX - offset;
            ctx.moveTo(drawStartX, drawStartY);
            ctx.lineTo(midX, drawStartY);
            let allY = [drawStartY, ...endPoints.map(p => p.y)];
            let minY = Math.min(...allY);
            let maxY = Math.max(...allY);
            ctx.moveTo(midX, minY);
            ctx.lineTo(midX, maxY);
            endPoints.forEach(p => {
                ctx.moveTo(midX, p.y);
                ctx.lineTo(p.x, p.y);
            });
        } else {
            let gap = (dir === 'bottom') ? fromNode.gaps.bottom * frSize : fromNode.gaps.top * frSize;
            let offset = gap / 2;
            let midY = (dir === 'bottom') ? drawStartY + offset : drawStartY - offset;
            ctx.moveTo(drawStartX, drawStartY);
            ctx.lineTo(drawStartX, midY);
            let allX = [drawStartX, ...endPoints.map(p => p.x)];
            let minX = Math.min(...allX);
            let maxX = Math.max(...allX);
            ctx.moveTo(minX, midY);
            ctx.lineTo(maxX, midY);
            endPoints.forEach(p => {
                ctx.moveTo(p.x, midY);
                ctx.lineTo(p.x, p.y);
            });
        }
        ctx.stroke();
        ctx.fillStyle = fromNode.settings.stroke;
        let angle = 0;
        if (dir === 'right') angle = 0;
        else if (dir === 'left') angle = Math.PI;
        else if (dir === 'bottom') angle = Math.PI / 2;
        else if (dir === 'top') angle = -Math.PI / 2;
        endPoints.forEach(p => {
            ctx.beginPath();
            ctx.moveTo(p.x, p.y);
            ctx.lineTo(p.x - 10 * Math.cos(angle - Math.PI / 6), p.y - 10 * Math.sin(angle - Math.PI / 6));
            ctx.lineTo(p.x - 10 * Math.cos(angle + Math.PI / 6), p.y - 10 * Math.sin(angle + Math.PI / 6));
            ctx.fill();
        });
    }
}
function animateEdges() {
    let start = performance.now();
    let ctx = document.getElementById('canvas-overlay').getContext('2d');
    function frame(time) {
        drawEdges(ctx, false);
        if (time - start < 350) requestAnimationFrame(frame);
    }
    requestAnimationFrame(frame);
}
function autoResizeAllFonts() {
    let maxMainSize = Math.max(1, Math.round(frSize * 0.6));
    let minMainSize = Math.max(1, Math.round(frSize * 0.2));
    document.querySelectorAll('.node').forEach(nodeEl => {
        let id = parseInt(nodeEl.dataset.id);
        let n = nodes.find(x => x.id === id);
        if (!n) return;
        let el = nodeEl.querySelector('.node-content');
        if (el) {
            let size = maxMainSize;
            el.style.fontSize = size + 'px';
            while ((el.scrollHeight > el.clientHeight || el.scrollWidth > el.clientWidth) && size > minMainSize) {
                size--;
                el.style.fontSize = size + 'px';
            }
            n.calculatedFontSize = size;
        }
        let noteEl = nodeEl.querySelector('.note-content');
        if (noteEl) {
            let maxNoteSize = Math.max(1, Math.round(frSize * 0.4));
            let minNoteSize = Math.max(1, Math.round(frSize * 0.15));
            let size = maxNoteSize;
            noteEl.style.fontSize = size + 'px';
            while ((noteEl.scrollHeight > noteEl.clientHeight || noteEl.scrollWidth > noteEl.clientWidth) && size > minNoteSize) {
                size--;
                noteEl.style.fontSize = size + 'px';
            }
            n.calculatedNoteFontSize = size;
        }
    });
}
