function triggerLoad(fileContent) {
    if (fileContent) {
        try {
            let jsonString = "";
            const trimmedContent = fileContent.trim();
            if (trimmedContent.startsWith('%7B%22nodes%22')) {

                console.warn("Phát hiện file định dạng cũ. Tiến hành khôi phục dự phòng...");
                jsonString = decodeURIComponent(trimmedContent);
            } else jsonString = decodeURIComponent(window.atob(trimmedContent));
            const data = JSON.parse(jsonString);
            nodes = data.nodes;
            edges = data.edges;
            idCounter = data.idCounter;
            cameraX = data.cameraX || window.innerWidth / 2;
            cameraY = data.cameraY || window.innerHeight / 2;
            frSize = data.frSize || 40;
            isDarkMode = data.isDarkMode !== undefined ? data.isDarkMode : true;
            nodes.forEach(n => {
                if (n.isCustomContent === undefined) {
                    const isRoot = !edges.some(e => e.to === n.id);
                    const defaultTexts = [i18n.en.rootNode, i18n.vi.rootNode, i18n.zh.rootNode, i18n.en.defaultNode, i18n.vi.defaultNode, i18n.zh.defaultNode];
                    n.isCustomContent = !defaultTexts.includes(n.content);
                }
            });
            if (data.currentLang) changeLanguage(data.currentLang);
            document.getElementById('sys-fr').value = Math.round(frSize);
            document.documentElement.style.setProperty('--fr-size', frSize + 'px');
            if (!isDarkMode) document.body.classList.add('light-mode');
            else document.body.classList.remove('light-mode');
            let defColors = getDefaultColors();
            nodes.forEach(n => {
                if (n.settings.isCustomColor === undefined) {
                    if (n.settings.bg.toLowerCase() !== defColors.bg.toLowerCase() ||
                        n.settings.stroke.toLowerCase() !== defColors.stroke.toLowerCase() ||
                        n.settings.color.toLowerCase() !== defColors.color.toLowerCase()) {
                        n.settings.isCustomColor = true;
                    } else n.settings.isCustomColor = false;
                }
            });
            recalculatePositions();
            render();
            showAlert(t('loadSuccess'));
        } catch (e) {
            console.error("Load error:", e);
            showAlert(t('loadFail'));
        }
    } else showAlert(t('loadFail'));
}
function executeExport() {
    const marginFr = parseFloat(document.getElementById('export-margin').value) || 0;
    const safeMargin = Math.max(marginFr * frSize, 20);
    if (nodes.length === 0) return;
    let originalTheme = isDarkMode;
    const toggleExportTheme = (toDark) => {
        isDarkMode = toDark;
        let defColors = getDefaultColors();
        nodes.forEach(n => {
            if (!n.settings.isCustomColor) {
                n.settings.bg = defColors.bg;
                n.settings.stroke = defColors.stroke;
                n.settings.color = defColors.color;
            }
        });
    };
    if (originalTheme) toggleExportTheme(false);
    try {
        let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
        nodes.forEach(n => {
            if (n.x < minX) minX = n.x;
            if (n.y < minY) minY = n.y;
            if (n.x + (n.width * frSize) > maxX) maxX = n.x + (n.width * frSize);
            let h = n.height * frSize;
            if (n.note.show) h += (n.settings.noteHeight * frSize) - 2;
            if (n.y + h > maxY) maxY = n.y + h;
        });
        const expCanvas = document.createElement('canvas');
        expCanvas.width = (maxX - minX) + safeMargin * 2;
        expCanvas.height = (maxY - minY) + safeMargin * 2;
        const ctx = expCanvas.getContext('2d');
        ctx.fillStyle = '#FFFFFF';
        ctx.fillRect(0, 0, expCanvas.width, expCanvas.height);
        ctx.save();
        ctx.translate(-minX + safeMargin, -minY + safeMargin);
        drawEdges(ctx, true);
        nodes.forEach(n => {
            let w = n.width * frSize;
            let h = n.height * frSize;
            let noteH = n.settings.noteHeight * frSize;
            ctx.fillStyle = n.settings.bg;
            ctx.fillRect(n.x, n.y, w, h);
            ctx.lineWidth = 2;
            ctx.strokeStyle = n.settings.stroke;
            ctx.strokeRect(n.x, n.y, w, h);
            let mainFontSize = n.calculatedFontSize || Math.round(frSize * 0.6);
            ctx.font = mainFontSize + 'px sans-serif';
            ctx.fillStyle = n.settings.color;
            ctx.textAlign = 'center';
            ctx.textBaseline = 'middle';
            let maxTextWidth = w - 20;
            let lineHeight = mainFontSize * 1.2;
            wrapText(ctx, n.content, n.x + w / 2, n.y + h / 2, maxTextWidth, lineHeight);
            if (n.note.show) {
                ctx.fillStyle = n.settings.bg;
                ctx.fillRect(n.x, n.y + h - 2, w, noteH);
                ctx.beginPath();
                ctx.moveTo(n.x, n.y + h - 2);
                ctx.lineTo(n.x, n.y + h - 2 + noteH);
                ctx.lineTo(n.x + w, n.y + h - 2 + noteH);
                ctx.lineTo(n.x + w, n.y + h - 2);
                ctx.stroke();
                ctx.beginPath();
                ctx.setLineDash([3, 3]);
                ctx.moveTo(n.x, n.y + h - 2);
                ctx.lineTo(n.x + w, n.y + h - 2);
                ctx.stroke();
                ctx.setLineDash([]);
                let noteFontSize = n.calculatedNoteFontSize || Math.round(frSize * 0.4);
                ctx.font = noteFontSize + 'px sans-serif';
                ctx.fillStyle = n.settings.color;
                let maxNoteWidth = w - 10;
                let noteLineHeight = noteFontSize * 1.2;
                wrapText(ctx, n.note.content, n.x + w / 2, n.y + h - 2 + noteH / 2, maxNoteWidth, noteLineHeight);
            }
        });
        ctx.restore();
        const link = document.createElement('a');
        link.download = 'TreeDiagram_Export.png';
        link.href = expCanvas.toDataURL('image/png');
        link.click();
    } finally { if (originalTheme) toggleExportTheme(true); }
}
function wrapText(ctx, text, x, y, maxWidth, lineHeight) {
    if (!text) return;
    let words = text.toString().split(' ');
    let line = '';
    let lines = [];
    for (let n = 0; n < words.length; n++) {
        let testLine = line + words[n] + ' ';
        let metrics = ctx.measureText(testLine);
        let testWidth = metrics.width;
        if (testWidth > maxWidth && n > 0) {
            lines.push(line);
            line = words[n] + ' ';
        } else line = testLine;
    }
    lines.push(line);
    let startY = y - ((lines.length - 1) * lineHeight) / 2;
    for (let i = 0; i < lines.length; i++) {
        ctx.fillText(lines[i].trim(), x, startY + (i * lineHeight));
    }
}
