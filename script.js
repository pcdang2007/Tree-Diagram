fileInput.addEventListener('change', function (e) {
    let file = e.target.files[0];
    if (!file) return;
    let reader = new FileReader();
    reader.onload = function (event) {
        triggerLoad(event.target.result);
    };
    reader.onerror = function () {
        showAlert(t('loadFail'));
    };
    reader.readAsText(file);
    fileInput.value = '';
});
function setupDrag(panelId, headerId) {
    const panel = document.getElementById(panelId);
    const header = document.getElementById(headerId);
    if (!panel || !header) return;
    header.addEventListener('mousedown', (e) => {
        activeDragPanel = panel;
        dragOffsetX = e.clientX - panel.getBoundingClientRect().left;
        dragOffsetY = e.clientY - panel.getBoundingClientRect().top;
        e.stopPropagation();
    });
}
function toggleTheme() {
    isDarkMode = !isDarkMode;
    if (isDarkMode) document.body.classList.remove('light-mode');
    else document.body.classList.add('light-mode');
    let defColors = getDefaultColors();
    nodes.forEach(n => {
        if (n.settings.isCustomColor === false || n.settings.isCustomColor === undefined) {
            n.settings.bg = defColors.bg;
            n.settings.stroke = defColors.stroke;
            n.settings.color = defColors.color;
            n.settings.isCustomColor = false;
        }
    });
    if (currentNodeId) {
        let currNode = nodes.find(n => n.id === currentNodeId);
        if (currNode) {
            document.getElementById('set-bg').value = currNode.settings.bg;
            document.getElementById('set-stroke').value = currNode.settings.stroke;
            document.getElementById('set-color').value = currNode.settings.color;
        }
    }
    render();
}
function getDefaultColors() {
    if (isDarkMode) return { bg: '#121212', stroke: '#FFFFFF', color: '#FFFFFF' };
    return { bg: '#FFFFFF', stroke: '#000000', color: '#000000' };
}
